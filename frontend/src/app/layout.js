const fetchGameCategories = async () => {
  try {
    const data = await fetch("http://localhost:3000/api/categories/all", {
      next: { revalidate: "60" },
      
    });
    const categories = await data.json();
    return categories;
  } catch (error) {
    console.log(error.message, "categories error");
  }
};

